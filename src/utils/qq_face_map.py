def get_face_description(face_id: int) -> str:
    """
    获取表情描述
    
    Args:
        face_id: QQ 表情 ID
        
    Returns:
        str: 表情的文字描述
    """
    return QQ_FACE_MAP.get(face_id, f"表情{face_id}")


def faces_to_text(faces: list) -> str:
    """
    将多个表情转换为文字描述
    
    Args:
        faces: 表情 ID 列表
        
    Returns:
        str: 组合的文字描述
    """
    if not faces:
        return ""
    
    descriptions = [get_face_description(face_id) for face_id in faces]
    return "[" + "".join(descriptions) + "]"
